import math
import cmath  # для комплексних чисел

# a) √81
a = math.sqrt(81)
print("a) √81 =", a)

# b) 810
b = 810
print("b) 810 =", b)

# c) √2 + √3 + √6
c = math.sqrt(2) + math.sqrt(3) + math.sqrt(6)
print("c) √2 + √3 + √6 =", c)

# d) √-5 (комплексне число)
d = cmath.sqrt(-5)
print("d) √-5 =", d)

# e) ³√125 (кубічний корінь)
e = math.pow(125, 1/3)
print("e) ³√125 =", e)